package org.febit.leancloud.client;

/**
 *
 * @author zqq90
 */
public class LcBasicResponse implements LcResponse, LcCreateResponse, LcUpdateResponse, LcDeleteResponse {

    protected int statusCode;
    protected int code;
    protected String error;

    protected String updatedAt;
    protected String createdAt;
    protected String objectId;

    public LcBasicResponse() {
    }

    @Override
    public int getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(int statusCode) {
        this.statusCode = statusCode;
    }

    @Override
    public boolean isOk() {
        return code == 0 && (statusCode == 0 || !LcApiClient.isRequestFailed(statusCode));
    }

    @Override
    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    @Override
    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    @Override
    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

    @Override
    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    @Override
    public String getObjectId() {
        return objectId;
    }

    public void setObjectId(String objectId) {
        this.objectId = objectId;
    }

    @Override
    public String toString() {
        return "LcBasicResponse{" + "statusCode=" + statusCode + ", code=" + code + ", error=" + error + '}';
    }
}
